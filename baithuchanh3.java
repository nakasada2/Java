    private void btquaphaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btquaphaiActionPerformed
        // TODO add your handling code here:
        int[] indexs = listtrai.getSelectedIndices();
        for(int i = indexs.length-1; i>=0;i--)
        {
            model12.addElement(model11.get(indexs[i]));
            model11.remove(indexs[i]);
        }
    }//GEN-LAST:event_btquaphaiActionPerformed

