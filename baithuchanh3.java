    private void btquaphaihetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btquaphaihetActionPerformed
        // TODO add your handling code here:
        int rt = model11.getSize();
        for(int i = 0; i < rt ; i++)
        {
            model12.addElement(model11.get(i));
        }
        model11.clear();
    }//GEN-LAST:event_btquaphaihetActionPerformed

